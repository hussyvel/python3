numero1 = input("Colocando o primeiro valor: ")
numero1 = int(numero1)

numero2 = input("Colocando o segundo valor: ")
numero2 = int(numero2)

if(numero1==numero2):#esse operador é operador de igualdade
    print("O valor digitado é %d é igual a %d" %(numero1, numero2))
if(numero1>=numero2):#esse operador é para maior igual que
    print("Nesse será para colocar %d é maior que ou igual que %d"%(numero1,numero2))
if(numero1<=numero2):#esse é menor que ou igual que
    print("Aqui %d é menor ou igual a %d"%(numero1,numero2))
if(numero1>numero2):# o primeiro é maior que o segundo numero
    print("o primeiro %d é maior que %d"%(numero1,numero2))
if(numero1<numero2): #esse o primeiro é menor que o segundo
    print("Esse primeiro %d é menor que %d"%(numero1,numero2))
if(numero1!=numero2):#aqui ficará apenas a diferença de um para o outro
    print("Neste o primeiro numero %d é diferente de %d"%(numero1, numero2))


